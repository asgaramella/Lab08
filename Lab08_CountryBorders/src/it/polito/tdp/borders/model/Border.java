package it.polito.tdp.borders.model;

public class Border {
	private int code1;
	private int code2;
	
	public Border(int code1, int code2) {
		super();
		this.code1 = code1;
		this.code2 = code2;
	}

	public int getCode1() {
		return code1;
	}

	public void setCode1(int code1) {
		this.code1 = code1;
	}

	public int getCode2() {
		return code2;
	}

	public void setCode2(int code2) {
		this.code2 = code2;
	}
	
	

}
